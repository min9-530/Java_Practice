public class Data_and_Operation {
    public static void main(String[] args) {
        System.out.println(6); // Number (숫자)
        System.out.println("six"); // String (문자열)

        System.out.println("6"); // String 6

        System.out.println(6 + 6); // 12
        System.out.println("6"+"6"); // 66

        System.out.println(6*6); // 36
//      System.out.println("6"*"6"); // 문자열은 곱하기 연산 불가능
        System.out.println("1111".length()); // 4 (length()는 문자열의 길이를 알려줌)
//      System.out.println(1111.length()); // 문자열이 아닌 숫자이므로 length()함수 사용 불가능!
    }
}